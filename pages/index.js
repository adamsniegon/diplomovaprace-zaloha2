export async function getStaticProps() {
  await dbConnect()

  let data;
  try {
    data = await Place.find();
    data = JSON.parse(JSON.stringify(data));
  } catch (error) {
    data = [
      {
        name: "Something went wrong"
      }
    ]
  }

  return {
    props: {
      data
    }
  };
}